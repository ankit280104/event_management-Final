import mongoose from "mongoose";

const clubSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    image: {
      type: String,
    },
  },
  { timestamps: true }
);

export const ClubModel = mongoose.model("Club", clubSchema);
